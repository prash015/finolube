				var getSingleLineValue = function(name) {
					return $.trim(String($form.find('[name="' + name + '"]').val() || '')).replace(/\s+/g, ' ');
				};
				var getMultiLineValue = function(name) {
					return $.trim(String($form.find('[name="' + name + '"]').val() || '')).replace(/\r\n/g, '\n').replace(/\r/g, '\n');
				};
				
				var userName = getSingleLineValue('username');
